public class ConditionExpression    {
    public static void main(String args[])	{
    
    int a = 7;
    
    if (a < 4)
	System.out.println("Less the 4");
    else    {
	System.out.println("Greater than 4");
    }
//below is a condtion expression
    System.out.println((a < 4) ? "Less than 4" : "Greater than 4");
    
    }
}
